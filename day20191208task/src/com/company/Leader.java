package com.company;

public class Leader {
    /*
    * 部门领导信息
    * */
    // 编号
    private String leaderId;

    // 姓名
    private String leaderName;

    // 性别
    private String leaderSex;

    // 年龄
    private String leaderAge;

    public String getLeaderId() {
        return leaderId;
    }

    public void setLeaderId(String leaderId) {
        this.leaderId = leaderId;
    }

    public String getLeaderName() {
        return leaderName;
    }

    public void setLeaderName(String leaderName) {
        this.leaderName = leaderName;
    }

    public String getLeaderSex() {
        return leaderSex;
    }

    public void setLeaderSex(String leaderSex) {
        this.leaderSex = leaderSex;
    }

    public String getLeaderAge() {
        return leaderAge;
    }

    public void setLeaderAge(String leaderAge) {
        this.leaderAge = leaderAge;
    }
}
